#range function
for x in range(10):
#range upto 10 comes
    print(x)
#print upto number 9 starting from 0
for x in range(10,20):
#print numbers from 10 to 19
    print(x)
for x in range(0,10,3):
#first argument starting point
#second argument ending point
#third argument increment
    print(x)
#using range function we can also create list
print(list(range(10)))
#list is created starting index from 0 to 9
